public class ExercicioControleFluxo2 {
    public static void main(String[] args) {
        byte dia = 1;

        switch (dia){
            case 1:
                System.out.println("Doingo - Final de semana");
                break;
            case 2:
                System.out.println("Doingo - Dia Util");
                break;
            case 3:
                System.out.println("Doingo - Dia Util");
                break;
            case 4:
                System.out.println("Doingo - Dia Util");
                break;
            case 5:
                System.out.println("Doingo - Dia Util");
                break;
            case 6:
                System.out.println("Doingo - Dia Util");
                break;
            case 7:
                System.out.println("Doingo - Final de semana");
                break;
        }
    }
}
